# @lc code=start
class Solution:
    def intToRoman(self, num: int) -> str:
        # creating dictionary
        num_maps = {
            1000: "M",
            900: "CM",
            500: "D",
            400: "CD",
            100: "C",
            90: "XC",
            50: "L",
            40: "XL",
            10: "X",
            9: "IX",
            5: "V",
            4: "IV",
            1: "I",
        }
        ans = ""
        for i in sorted(num_maps.keys(), reverse=True):
            mod, num = divmod(num, i)
            ans += num_maps[i] * mod
        return ans
        
# @lc code=end

